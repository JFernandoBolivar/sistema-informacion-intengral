/**
 * Interface for inventory article
 */
export interface InventoryArticle {
  id: string;
  codigo_articulo: string;
  tipo_insumo: string;
  categoria: string;
  descripcion: string;
  estado: ArticleStatus | string;
  numero_factura: string;
  inventario_total: number;
  inventario_entrega: number;
  fecha_adquisicion: string;
  fecha_mantenimiento?: string;
  
  // Optional fields that might be used in the UI
  created_at?: string;
  updated_at?: string;
}

/**
 * Interface for inventory article creation
 * Omits the ID field which is generated by the server
 */
export type CreateInventoryArticle = Omit<InventoryArticle, "id">;

/**
 * Interface for inventory article update
 * All fields are optional
 */
export type UpdateInventoryArticle = Partial<InventoryArticle>;

/**
 * Interface for inventory summary stats
 */
export interface InventorySummary {
  total_items: number;
  available_items: number;
  delivered_items: number;
  by_category: {
    [key: string]: number;
  };
}

